#include <iostream>
using namespace std;

int main() {
	setlocale(LC_ALL, "Portuguese");
	
    int num, soma = 0;

    cout << "Digite um número inteiro: ";
    cin >> num;

    while (num != 0) {
        soma += num % 10;
        num /= 10;
  }

    cout << "A soma dos dígitos é " << soma << endl;

    return 0;
}
